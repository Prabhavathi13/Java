package com.practice_1;

public class Datatypes {
	static void myMethod() {
		int i = 234 ;         // this a integer datatype
		char c = 'A' ;        // this a char datatype
		float f = 3.5f ;      // this a float datatype
		double d = 44.32 ;    // this a double datatype
		boolean b = true ;    // this a boolean datatype
		String s = "Jala" ;   // this a String datatype
		
		System.out.println("This is a interger : " + i );
		System.out.println("This is a char : " + c );
		System.out.println("This is a float : " + f );
		System.out.println("This is a double : " + d );
		System.out.println("This is a boolean : " + b );
		System.out.println("This is a String : " + s );
		
	}
	

	public static void main(String[] args) {
		
		myMethod();
		}

}
